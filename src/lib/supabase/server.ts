// Client Supabase côté serveur (Server Components, Route Handlers, Server Actions)
// Utilise createServerClient de @supabase/ssr avec les cookies de next/headers

import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // Impossible de modifier les cookies depuis un Server Component
            // Cela arrive quand le client Supabase est utilisé en lecture seule
            // dans un Server Component — les cookies seront mis à jour
            // par le middleware ou le Route Handler suivant
          }
        },
      },
    }
  );
}
